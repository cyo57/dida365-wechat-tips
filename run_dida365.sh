#!/bin/bash

# 滴答清单任务推送脚本
# 用于 cron 定时执行

echo "============================================"
echo "开始执行滴答清单任务推送..."
echo "时间: $(date)"
echo "============================================"


# 执行 Docker 容器
echo "执行 Docker 容器..."
podman compose up --build

# 检查执行结果
if [ $? -eq 0 ]; then
    echo "============================================"
    echo "✅ 推送执行成功!"
    echo "完成时间: $(date)"
    echo "============================================"
else
    echo "============================================"
    echo "❌ 推送执行失败!"
    echo "失败时间: $(date)"
    echo "============================================"
    # 发送失败通知（可选）
    # 这里可以添加失败通知逻辑
fi